public class Ausgabe {
  public static void main(String[] args) {
    ausgabe(1, "Mana");
    ausgabe(2, "Elise");
    ausgabe(3, "Johanna");
    ausgabe(4, "Felizitas");
    ausgabe(5, "Karla");

  }

  public static void ausgabe(int zahl, String name) {
    System.out.println(zahl + ": " + name);
  }
}

